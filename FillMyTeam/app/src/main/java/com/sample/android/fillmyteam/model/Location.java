
package com.sample.android.fillmyteam.model;

public class Location {

    private Float lat;
    private Float lng;

    /**
     * 
     * @return
     *     The lat
     */
    public Float getLat() {
        return lat;
    }

    /**
     * 
     * @param lat
     *     The lat
     */
    public void setLat(Float lat) {
        this.lat = lat;
    }

    /**
     * 
     * @return
     *     The lng
     */
    public Float getLng() {
        return lng;
    }

    /**
     * 
     * @param lng
     *     The lng
     */
    public void setLng(Float lng) {
        this.lng = lng;
    }

}
